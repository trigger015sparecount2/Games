//*****************************************************************************
//初期化
//*****************************************************************************
HRESULT CUI::Init(D3DXVECTOR3 pos, D3DXVECTOR2 size, int nType, D3DXCOLOR col, D3DXVECTOR3 rot)
{
	CScene2D::Init(pos);
	AddRot(rot.y);
	SetTexture(nType);	//選択した番号のテクスチャを貼る
	SetSize(size);
	ColorChange(col);
	Set();
	return S_OK;
}

//*****************************************************************************
//終了
