CUI *CUI::Create(D3DXVECTOR3 pos, D3DXVECTOR2 size, int nType, D3DXCOLOR col, D3DXVECTOR3 rot)
{
	CUI *pUI = NULL;
	pUI = new CUI(PRIORITY_UI);
	if (pUI != NULL)
	{
		pUI->Init(pos, size, nType, col, rot);
	}
	return pUI;
}
